import java.util.Scanner;
/**
 * Ввести с консоли 3 целых числа.
 * На консоль вывести: если их значения упорядочены по возрастанию, то удвоить их;
 * в противном случае заменить значение каждой переменной на противоположное.
 * Вывести новые значения переменных.
 * @autor Огородников Лев
 */
public class Main {
    /**
     * Точка входа в программу.
     * Считывает три целых числа с консоли и изменяет их значения в зависимости от того,
     * упорядочены ли они по возрастанию.
     * Выводит новые значения переменных на консоль.
     * @param args аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        System.out.print("Введите три целых числа: ");
        int a = scanner.nextInt();
        int b = scanner.nextInt();
        int c = scanner.nextInt();

        if (a <= b && b <= c) {
            a *= 2;
            b *= 2;
            c *= 2;
        } else {
            a = -a;
            b = -b;
            c = -c;
        }

        System.out.printf("Новые значения: a = %d, b = %d, c = %d%n", a, b, c);
    }
}